#!/usr/bin/env python3
"""
Define a type-annotated function to return
the string representation for a float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation for
    any given floating point value.

    Parameters
    n : float
        The floating point number.

    Return
        The string representation
        for the given value.
    """
    return (str(n))
