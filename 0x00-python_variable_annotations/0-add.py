#!/usr/bin/env python3
"""
Define a type-annotated function that returns
the sum of two floating numbers.
"""


def add(a: float, b: float) -> float:
    """
    Add the two given floating point numbers.

    Parameters
    a : float
        The first number.
    b : float
        The second number.

    Return
        The sum of the two floating
        point numbers.
    """
    return (a + b)
